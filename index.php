                  Stávající kobercové mostní závěry, byly v prázdninových měsících nahrazeny novými ocelovými lamelového typu. Touto výměnou byla
                  odstraněna kritická závada související se zatékáním srážkové vody do prostoru tubusu nosné konstrukce a následně ohrožující provoz metra.
