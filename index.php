          <h3>Generální oprava Nuselského mostu</h3>
          <p>
              Nuselský most, který je důležitou pražskou dopravní tepnou, prošel generální opravou.
              Stavbu realizovalo sdružení firem STRABAG a SMP CZ. Do poloviny prosince budou na mostě probíhat dokončovací práce,
              řidičům bude ale v plném rozsahu sloužit již od 16. listopadu. V rámci opravy proběhla kompletní výměna mostního svršku
              a sanace betonových ploch nosné konstrukce a spodní stavby.
          </p>
          <p>
              Diagnostickými průzkumy provedenými do roku 2010 byla zjištěna celá řada závad. Zásadním problémem provozovaného mostu bylo zatékání srážkové
              vody do tubusu nosné konstrukce v místech dilatačních a pracovních spár. Srážková voda vnikala do prostorů s elektroinstalací metra a ohrožovala jeho provoz.
              Další kritickou závadou byla nefunkční hydroizolace chodníkových částí nosné konstrukce. Zde přes spáry chodníkových panelů docházelo k zatékání vody na trámové
              konzoly nosné konstrukce a následovala degradace betonu a hloubková koroze betonářské výztuže. Zjištěné poruchy významně ovlivňovaly životnost mostu,
              proto bylo v roce 2010 rozhodnuto provést kompletní rekonstrukci celého mostního objektu.
          </p>
              <p>
